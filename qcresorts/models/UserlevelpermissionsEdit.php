    // Load form values
    protected function loadFormValues()
    {
        // Load from form
        global $CurrentForm;
        $validate = !Config("SERVER_VALIDATE");

        // Check field name 'userlevelid' first before field var 'x_userlevelid'
        $val = $CurrentForm->hasValue("userlevelid") ? $CurrentForm->getValue("userlevelid") : $CurrentForm->getValue("x_userlevelid");
        if (!$this->userlevelid->IsDetailKey) {
            if (IsApi() && $val === null) {
                $this->userlevelid->Visible = false; // Disable update for API request
            } else {
                $this->userlevelid->setFormValue($val, true, $validate);
            }
        }
        if ($CurrentForm->hasValue("o_userlevelid")) {
            $this->userlevelid->setOldValue($CurrentForm->getValue("o_userlevelid"));
        }

        // Check field name 'tablename' first before field var 'x__tablename'
        $val = $CurrentForm->hasValue("tablename") ? $CurrentForm->getValue("tablename") : $CurrentForm->getValue("x__tablename");
        if (!$this->_tablename->IsDetailKey) {
            if (IsApi() && $val === null) {
                $this->_tablename->Visible = false; // Disable update for API request
            } else {
                $this->_tablename->setFormValue($val);
            }
        }
        if ($CurrentForm->hasValue("o__tablename")) {
            $this->_tablename->setOldValue($CurrentForm->getValue("o__tablename"));
        }

        // Check field name 'permission' first before field var 'x__permission'
        $val = $CurrentForm->hasValue("permission") ? $CurrentForm->getValue("permission") : $CurrentForm->getValue("x__permission");
        if (!$this->_permission->IsDetailKey) {
            if (IsApi() && $val === null) {
                $this->_permission->Visible = false; // Disable update for API request
            } else {
                $this->_permission->setFormValue($val, true, $validate);
            }
        }
    }

